// ---------------------------------------------------------------------------
// Imports
// ---------------------------------------------------------------------------

import 'dart:math';
import 'dart:typed_data';

// ---------------------------------------------------------------------------
// Secure Random
// ---------------------------------------------------------------------------

class CamoSecureRandom {
  // ---------------------------------------------------------------------------
  // Constructor
  // ---------------------------------------------------------------------------

  CamoSecureRandom() : _random = Random.secure();

  // ---------------------------------------------------------------------------
  // Properties
  // ---------------------------------------------------------------------------

  final Random _random;

  // ---------------------------------------------------------------------------
  // Generate Bytes
  // ---------------------------------------------------------------------------

  Uint8List generateBytes(int length) {
    return Uint8List.fromList(
      List<int>.generate(
        length,
        (_) => _random.nextInt(256),
      ),
    );
  }
}