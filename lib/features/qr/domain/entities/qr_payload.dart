// ---------------------------------------------------------------------------
// Class
// ---------------------------------------------------------------------------

class QrPayload {
  // ---------------------------------------------------------------------------
  // Constructor
  // ---------------------------------------------------------------------------

  const QrPayload({
    required this.version,
    required this.type,
    required this.identity,
  });

  // ---------------------------------------------------------------------------
  // Properties
  // ---------------------------------------------------------------------------

  final int version;
  final String type;
  final String identity;

  // ---------------------------------------------------------------------------
  // Public Methods
  // ---------------------------------------------------------------------------

  Map<String, dynamic> toJson() {
    return <String, dynamic>{
      'v': version,
      't': type,
      'id': identity,
    };
  }
}