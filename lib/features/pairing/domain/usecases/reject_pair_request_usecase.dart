// ---------------------------------------------------------------------------
// Imports
// ---------------------------------------------------------------------------

import '../repositories/pairing_repository.dart';

// ---------------------------------------------------------------------------
// Reject Pair Request Use Case
// ---------------------------------------------------------------------------

class RejectPairRequestUseCase {
  // ---------------------------------------------------------------------------
  // Constructor
  // ---------------------------------------------------------------------------

  const RejectPairRequestUseCase(this._repository);

  // ---------------------------------------------------------------------------
  // Dependencies
  // ---------------------------------------------------------------------------

  final PairingRepository _repository;

  // ---------------------------------------------------------------------------
  // Call
  // ---------------------------------------------------------------------------

  Future<void> call(String pairingId) {
    return _repository.rejectPairRequest(pairingId);
  }
}