// ---------------------------------------------------------------------------
// Imports
// ---------------------------------------------------------------------------

import '../entities/pairing_entity.dart';
import '../repositories/pairing_repository.dart';

// ---------------------------------------------------------------------------
// Watch Pending Pair Requests Use Case
// ---------------------------------------------------------------------------

class WatchPendingPairRequestsUseCase {
  // ---------------------------------------------------------------------------
  // Constructor
  // ---------------------------------------------------------------------------

  const WatchPendingPairRequestsUseCase(this._repository);

  // ---------------------------------------------------------------------------
  // Dependencies
  // ---------------------------------------------------------------------------

  final PairingRepository _repository;

  // ---------------------------------------------------------------------------
  // Call
  // ---------------------------------------------------------------------------

  Stream<List<PairingEntity>> call(String receiverUid) {
    return _repository.watchPendingRequests(receiverUid);
  }
}